#define  TRY  1000
#define  DEG   100

//====================================================================
//  大域宣言
//====================================================================


//====================================================================
//  本体処理
//====================================================================

int main(void) 
{
  //----  宣言
  int m;           // 試行数
  int n;           // データ数
  int arr[DEG];    // 乱順列
  int k;           // 反復変数
  double p;        // 確率指定
  int c;           // 回数指定

  //----  入力
  scanf("%d", &m);
  // m = TRY;
  scanf("%d", &n);
  // n = DEG;
  scanf("%lf", &p);
  // p = 0.5;
  // scanf("%d", &c);
  // c = 10;

  //----  初期処理
  srand((unsigned) time(NULL));    // 時間関数による乱数の初期化

  //----  全体の反復
  while ( m-- > 0 ) {
    //--  配列の初期化
    for ( k = 0; k < n; k++ ) {
      arr[k] = k;
    }
    //--  乱順列
    prand(arr, n, p);    // 確率指定
    // crand(arr, n, c);    // 回数指定
    //--  出力
    for ( k = 0; k < n; k++ ) {
      printf("%2d ", arr[k]);
    }
    puts("");
  }

  //----  終了
  return 0;
}


